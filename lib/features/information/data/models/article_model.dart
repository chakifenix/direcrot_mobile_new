import 'package:direcrot_mobile_new/features/information/domain/entities/article.dart';

class ArticleModel extends Article {
  ArticleModel(
      {required super.id,
      required super.referal,
      required super.lang,
      required super.menu,
      required super.submenu,
      required super.title,
      required super.annot,
      required super.text,
      required super.filename,
      required super.images,
      required super.attachments,
      required super.date,
      required super.time,
      required super.datetime,
      required super.views,
      required super.moderator,
      required super.ip,
      required super.browser,
      required super.status});

  factory ArticleModel.fromjson(Map<String, dynamic> json) => ArticleModel(
        id: json["id"],
        referal: json["referal"],
        lang: json["lang"],
        menu: json["menu"],
        submenu: json["submenu"],
        title: json["title"],
        annot: json["annot"],
        text: json["text"],
        filename: json["filename"],
        images: json["images"],
        attachments: json["attachments"],
        date: DateTime.parse(json["date"]),
        time: json["time"],
        datetime: DateTime.parse(json["datetime"]),
        views: json["views"],
        moderator: json["moderator"],
        ip: json["ip"],
        browser: json["browser"],
        status: json["status"],
      );

  ArticleModel copyWith({
    String? id,
    String? referal,
    String? lang,
    String? menu,
    String? submenu,
    String? title,
    String? annot,
    String? text,
    String? filename,
    String? images,
    String? attachments,
    DateTime? date,
    String? time,
    DateTime? datetime,
    String? views,
    String? moderator,
    String? ip,
    dynamic browser,
    String? status,
  }) =>
      ArticleModel(
        id: id ?? this.id,
        referal: referal ?? this.referal,
        lang: lang ?? this.lang,
        menu: menu ?? this.menu,
        submenu: submenu ?? this.submenu,
        title: title ?? this.title,
        annot: annot ?? this.annot,
        text: text ?? this.text,
        filename: filename ?? this.filename,
        images: images ?? this.images,
        attachments: attachments ?? this.attachments,
        date: date ?? this.date,
        time: time ?? this.time,
        datetime: datetime ?? this.datetime,
        views: views ?? this.views,
        moderator: moderator ?? this.moderator,
        ip: ip ?? this.ip,
        browser: browser ?? this.browser,
        status: status ?? this.status,
      );
}
